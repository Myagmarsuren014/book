<script>
    document.addEventListener("DOMContentLoaded", function() {
        const slider = document.getElementById("product-slider");
        const nextBtn = document.getElementById("next-btn");
        const prevBtn = document.getElementById("prev-btn");
        const dotsContainer = document.getElementById("dots-container");
        const slides = document.querySelectorAll(".single-slide");
        let currentIndex = 0;
        let autoSlide;

        const getCardsPerSlide = () => {
            if (window.innerWidth >= 1024) {
                return 4; // Desktop
            } else if (window.innerWidth >= 768) {
                return 3; // Tablet
            } else {
                return 1; // Mobile
            }
        };

        let cardsPerSlide = getCardsPerSlide();
        const totalSlides = Math.ceil(slides.length / cardsPerSlide);

        // Initialize Dots
        const initializeDots = () => {
            dotsContainer.innerHTML = "";
            for (let i = 0; i < totalSlides; i++) {
                const dot = document.createElement("div");
                dot.classList.add("dot");
                if (i === 0) dot.classList.add("active");
                dot.addEventListener("click", () => moveToSlide(i));
                dotsContainer.appendChild(dot);
            }
        };

        // Navigation Button Handlers
        nextBtn.addEventListener("click", () => moveToSlide(currentIndex + 1));
        prevBtn.addEventListener("click", () => moveToSlide(currentIndex - 1));

        // Move to Slide Function
        const moveToSlide = (index) => {
            if (index < 0) {
                currentIndex = totalSlides - 1;
            } else if (index >= totalSlides) {
                currentIndex = 0;
            } else {
                currentIndex = index;
            }
            slider.scrollTo({
                left: slider.offsetWidth * currentIndex,
                behavior: "smooth",
            });
            updateDots();
        };

        // Update Active Dot
        const updateDots = () => {
            document.querySelectorAll(".dot").forEach((dot, index) => {
                dot.classList.toggle("active", index === currentIndex);
            });
        };

        // Auto Slide
        const startAutoSlide = () => {
            autoSlide = setInterval(() => {
                moveToSlide(currentIndex + 1);
            }, 4000);
        };

        const stopAutoSlide = () => clearInterval(autoSlide);

        // Responsive Handling
        const handleResize = () => {
            cardsPerSlide = getCardsPerSlide();
            initializeDots();
            currentIndex = 0;
            moveToSlide(0);
        };

        window.addEventListener("resize", handleResize);

        // Initialize on Load
        handleResize();
        startAutoSlide();

        slider.addEventListener("mouseover", stopAutoSlide);
        slider.addEventListener("mouseout", startAutoSlide);
    });
</script>
